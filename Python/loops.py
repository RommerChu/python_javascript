print("-------For Loop: Array numbers-------")

for i in [0, 1, 2, 3, 4, 5]:
    print(i)

print("-------For Loop: Range function-------")

# Range of numbers
for i in range(7):
    print(i)

print("-------For Loop: Names in Array-------")

names= ["Rommer", "Corazon", "Peter", "Abigail", "John"]
print(names)

for name in names:
    print(name)

print("-------For Loop in name-------")

name = "Rommer"
for character in name:
    print(character)
